export const getDtcDescription = (code) => {
    if (!code) return null;
    const upper = String(code).toUpperCase().trim();
    return DTC_DESCRIPTIONS[upper] || null;
};

export const getDtcSeverityColor = (severitate) => {
    switch ((severitate || '').toUpperCase()) {
        case 'CRITICAL': return '#FF3B30';
        case 'WARNING':  return '#FF9500';
        default:         return '#8E8E93';
    }
};
